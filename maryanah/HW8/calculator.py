def add(num1, num2):
    """This function calculates sum of entered numbers."""
    sum = num1 + num2
    return sum

def subtraction(num1, num2):
    """This function calculates substraction
    of two entered numbers."""
    sub = num1 - num2
    return sub

def multiplication(num1, num2):
    """This function calculates multiplication
    of two entered numbers."""
    mult = num1 * num2
    return mult

def division(num1, num2):
    """This function calculates division
    of two entered numbers."""
    div = num1 / num2
    return div
